function btnOver(element) {
    element.style.backgroundColor = "hsl(202, 77%, 75%)";
    element.style.color = "white";
    // element.style.borderColor = "hsl(202, 57%, 49%)";  
    element.style.borderColor = "hsl(202, 77%, 75%)";  
}
    
function btnOut(element) {
    element.style.backgroundColor = "lightgray";  
    element.style.color = "black";
    element.style.borderColor = "lightgray";
}